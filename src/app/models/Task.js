import mongoose from 'mongoose'
const { Schema, model } = mongoose

const taskSchema = new Schema({
  title: String,
  author: String,
  body: String,
  comments: [{ body: String, date: Date }],
  date: { type: Date, default: Date.now },
  hidden: Boolean,
  meta: {
    votes: Number,
    favs: Number,
  },
})

export default model('Task', taskSchema)
